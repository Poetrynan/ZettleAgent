/**
 * 将 KaTeX CSS 中的字体引用替换为 base64 data URI，生成完全自包含的 CSS。
 * 运行：node scripts/inline-katex-css.cjs
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
// Prefer installer resources (source of truth), fall back to public/
const RES_CSS = path.join(ROOT, 'src-tauri', 'resources', 'webview', 'css', 'katex.min.css');
const RES_FONTS = path.join(ROOT, 'src-tauri', 'resources', 'webview', 'css', 'fonts');
const PUB_CSS = path.join(ROOT, 'public', 'css', 'katex.min.css');
const PUB_FONTS = path.join(ROOT, 'public', 'css', 'fonts');
const CSS_PATH = fs.existsSync(RES_CSS) ? RES_CSS : PUB_CSS;
const FONTS_DIR = fs.existsSync(RES_FONTS) ? RES_FONTS : PUB_FONTS;
const OUT_PATH = path.join(ROOT, 'src', 'lib', 'katex-inlined.css.ts');

function mimeType(ext) {
  const map = { '.woff2': 'font/woff2', '.woff': 'font/woff', '.ttf': 'font/ttf', '.eot': 'application/vnd.ms-fontobject' };
  return map[ext] || 'application/octet-stream';
}

function main() {
  let css = fs.readFileSync(CSS_PATH, 'utf-8');

  // 匹配 url(fonts/KaTeX_*.woff2) 等
  const urlRegex = /url\(fonts\/(KaTeX_[^)]+)\)/g;
  let match;
  const seen = new Set();

  while ((match = urlRegex.exec(css)) !== null) {
    const fontFile = match[1]; // e.g. KaTeX_AMS-Regular.woff2
    if (seen.has(fontFile)) continue;
    seen.add(fontFile);

    const fontPath = path.join(FONTS_DIR, fontFile);
    if (!fs.existsSync(fontPath)) {
      console.warn(`  ⚠ font not found: ${fontPath}`);
      continue;
    }

    const ext = path.extname(fontFile);
    const mime = mimeType(ext);
    const buf = fs.readFileSync(fontPath);
    const b64 = buf.toString('base64');
    const dataUri = `data:${mime};base64,${b64}`;

    // Replace ALL occurrences of this font file
    css = css.split(`url(fonts/${fontFile})`).join(`url(${dataUri})`);
    console.log(`  ✓ embedded: ${fontFile} (${(buf.length / 1024).toFixed(1)}KB)`);
  }

  // Write as a TS module that exports the CSS string
  const tsContent = `// Auto-generated by scripts/inline-katex-css.cjs — DO NOT EDIT
export const KATEX_CSS = ${JSON.stringify(css)};
`;
  fs.writeFileSync(OUT_PATH, tsContent, 'utf-8');
  console.log(`\n✓ Written to ${OUT_PATH} (${(fs.statSync(OUT_PATH).size / 1024).toFixed(1)}KB)`);
}

main();
